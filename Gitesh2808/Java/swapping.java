// Java program to display the swapping of two numbers 

        class swap
        {
          public static void main(String args[])
          {
            int a, b, c;
            a = 10;
            b = 20;
            c = a;
            a = b;
            b = c;
            System.out.print(a);
            System.out.print(b);
          }
        }
