// C program to find sum of two numbers..

#include <stdio.h>
#include <stdlib.h>
int main()
{
  int x, y, z;

  printf("Enter two integers : \n");
  scanf("%d%d", &x, &y);

  z = x + y;

  printf("Sum of two numbers = %d", z);

  return 0;
}

/*
Output :

    Enter two integers : 
    5   3
    Sum of two numbers = 8
*/
